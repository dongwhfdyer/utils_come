"""
Build an HTML report to compare captions generated by different models.

Usage:
  python build_caption_report.py \
    --input /path/to/captions.jsonl \
    --output /path/to/caption_report.html

Expect input JSONL lines with keys: id, model, features, caption
Models are ordered by model_list.txt if present.
"""

import argparse
import json
import os
from typing import Dict, List, Any, Tuple


def load_jsonl(path: str) -> List[Dict[str, Any]]:
    rows: List[Dict[str, Any]] = []
    with open(path, 'r') as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            try:
                rows.append(json.loads(line))
            except json.JSONDecodeError:
                # Skip malformed lines
                continue
    return rows


def load_models_order(path: str) -> List[str]:
    if not os.path.exists(path):
        return []
    out: List[str] = []
    with open(path, 'r') as f:
        for line in f:
            name = line.strip()
            if name:
                out.append(name)
    return out


def group_by_id(rows: List[Dict[str, Any]]) -> Dict[str, List[Dict[str, Any]]]:
    grouped: Dict[str, List[Dict[str, Any]]] = {}
    for r in rows:
        sid = str(r.get('id'))
        grouped.setdefault(sid, []).append(r)
    return grouped


def render_html(grouped: Dict[str, List[Dict[str, Any]]], model_order: List[str]) -> str:
    # Determine unique models present
    models_present = set()
    for items in grouped.values():
        for r in items:
            m = r.get('model')
            if m is not None:
                models_present.add(m)

    ordered_models: List[str] = [m for m in model_order if m in models_present]
    for m in sorted(models_present):
        if m not in ordered_models:
            ordered_models.append(m)

    # Basic style and interactive search/filter
    head = """
<!DOCTYPE html>
<html lang=\"en\">
<head>
  <meta charset=\"UTF-8\" />
  <meta name=\"viewport\" content=\"width=device-width, initial-scale=1.0\" />
  <title>Caption Comparison Report</title>
  <style>
    body { font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', Roboto, sans-serif; margin: 24px; color: #222; }
    h1 { margin-bottom: 8px; }
    .meta { color: #666; margin-bottom: 16px; }
    .controls { display: flex; gap: 12px; margin: 16px 0; align-items: center; }
    input[type="text"] { padding: 8px 10px; width: 360px; }
    select { padding: 6px 8px; }
    .sample { border: 1px solid #e5e7eb; border-radius: 8px; padding: 16px; margin-bottom: 16px; }
    .sample-header { display: flex; justify-content: space-between; align-items: baseline; margin-bottom: 12px; }
    .sid { font-weight: 600; }
    .grid { display: grid; gap: 12px; }
    .card { border: 1px solid #e5e7eb; border-radius: 8px; padding: 12px; background: #fafafa; }
    .model { font-weight: 600; margin-bottom: 6px; font-size: 14px; color: #374151; }
    .caption { white-space: pre-wrap; line-height: 1.45; }
    .muted { color: #6b7280; }
    .tag { display: inline-block; background: #eef2ff; color: #3730a3; border: 1px solid #c7d2fe; border-radius: 999px; padding: 2px 8px; font-size: 12px; margin-left: 8px; }
  </style>
  <script>
    function applyFilters() {
      const q = document.getElementById('search').value.toLowerCase();
      const model = document.getElementById('model').value;
      const samples = document.querySelectorAll('.sample');
      samples.forEach(s => {
        const text = s.innerText.toLowerCase();
        const modelCards = model ? s.querySelectorAll('.card[data-model="' + model + '"]') : null;
        const matchesQuery = !q || text.includes(q);
        const matchesModel = !model || (modelCards && modelCards.length > 0);
        s.style.display = (matchesQuery && matchesModel) ? '' : 'none';
      });
    }
  </script>
</head>
<body>
  <h1>Caption Comparison</h1>
  <div class=\"meta\">Compare generated captions across models for each sample id.</div>
  <div class=\"controls\">
    <input type=\"text\" id=\"search\" placeholder=\"Search captions or sample ids...\" oninput=\"applyFilters()\" />
    <label for=\"model\">Filter by model:</label>
    <select id=\"model\" onchange=\"applyFilters()\">
      <option value=\"\">All models</option>
"""

    for m in ordered_models:
        head += f"      <option value=\"{m}\">{m}</option>\n"

    head += """
    </select>
  </div>
"""

    body_parts: List[str] = []
    for sid in sorted(grouped.keys()):
        rows = grouped[sid]

        # Build an ordered list of records: for each model in order, include all rows for that model
        per_model: Dict[str, List[Dict[str, Any]]] = {}
        extras_present: set = set()
        for r in rows:
            m = r.get('model')
            if m is None:
                m = "<default>"
            per_model.setdefault(m, []).append(r)
            extras_present.add(m)

        sequence: List[str] = [m for m in ordered_models if m in extras_present]
        for m in sorted(extras_present):
            if m not in sequence:
                sequence.append(m)

        # Create a card for every record (not just first per model)
        cards_html: List[str] = []
        for m in sequence:
            records = per_model.get(m, [])
            display_model = m if m != "<default>" else "default"
            if not records:
                # still render empty placeholder card
                cards_html.append(
                    f"<div class=\"card\" data-model=\"{display_model}\"><div class=\"model\">{display_model}</div><div class=\"caption\">\u2014</div></div>"
                )
                continue
            for idx, rec in enumerate(records, start=1):
                caption = rec.get('caption') or "\u2014"
                cards_html.append(
                    f"<div class=\"card\" data-model=\"{display_model}\"><div class=\"model\">{display_model} <span class=\"muted\">#{idx}</span></div><div class=\"caption\">{caption}</div></div>"
                )

        # responsive grid to accommodate arbitrary number of cards
        grid_style = "grid-template-columns: repeat(auto-fit, minmax(260px, 1fr));"
        body_parts.append(
            f"<div class=\"sample\"><div class=\"sample-header\"><div class=\"sid\">{sid}</div><div class=\"muted\">Cards: {len(cards_html)}<span class=\"tag\">{', '.join(sequence)}</span></div></div><div class=\"grid\" style=\"{grid_style}\">{''.join(cards_html)}</div></div>"
        )

    tail = """
</body>
</html>
"""

    return head + "\n".join(body_parts) + tail


def main():
    parser = argparse.ArgumentParser(description="Build HTML comparison report from captions.jsonl")
    parser.add_argument("--input", type=str, required=True, help="Path to captions.jsonl")
    parser.add_argument("--output", type=str, required=True, help="Output HTML path")
    args = parser.parse_args()

    rows = load_jsonl(args.input)
    grouped = group_by_id(rows)
    # Look for model_list.txt in the docs directory
    project_root = os.path.join(os.path.dirname(__file__), '../..')
    model_list_path = os.path.join(project_root, 'docs/model_list.txt')
    models_order = load_models_order(model_list_path)
    html = render_html(grouped, models_order)

    with open(args.output, 'w') as f:
        f.write(html)

    print(f"Wrote report to {args.output} with {len(grouped)} samples")


if __name__ == "__main__":
    main()


